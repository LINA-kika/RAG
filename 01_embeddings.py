"""
01_embeddings.py — Знакомство с эмбеддингами
═══════════════════════════════════════════════════════════════════
Цель: понять что эмбеддинг — это просто вектор чисел, а сходство
двух текстов — это косинус угла между двумя такими векторами.

Что делаем:
  1. Берём 6 коротких фраз на русском.
  2. Получаем эмбеддинг каждой через LM Studio /v1/embeddings.
  3. Считаем матрицу 6×6 косинусных сходств.
  4. Выводим её в виде красивой таблицы с цветной подсветкой.
  5. Делаем мини-семантический поиск: «найди самое похожее
     на запрос "домашнее животное"».

Требования:
  - LM Studio запущен (порт 1234)
  - Загружена embedding-модель (например, nomic-embed-text-v1.5)
  - pip install openai numpy

Запуск: python 01_embeddings.py
"""

import numpy as np
from openai import OpenAI

# ********************* КОНФИГУРАЦИЯ *********************
LM_STUDIO_URL   = "http://127.0.0.1:1234/v1"
EMBEDDING_MODEL = "text-embedding-nomic-embed-text-v1.5"   # как видит её LM Studio

# Несколько фраз для эксперимента — нарочно подобраны так, чтобы
# было видно семантическое сходство (фразы про животных близки между собой,
# про транспорт — между собой, а группы далеки друг от друга)
PHRASES = [
    "Кошка спит на коврике",                # 0 — про кошку
    "Котёнок дремлет на ковре",             # 1 — почти то же другими словами
    "Собака охраняет дом",                  # 2 — другое животное
    "Самолёт взлетает с полосы",            # 3 — транспорт
    "Поезд отправляется со станции",        # 4 — транспорт, синонимично 3
    "Программист пишет код в редакторе",    # 5 — совсем другая тема
]

# Запрос для семантического поиска
QUERY = "домашнее животное"


# ********************* ИНИЦИАЛИЗАЦИЯ *********************

# api_key='lm-studio' — LM Studio не проверяет ключ, но библиотека требует
client = OpenAI(base_url=LM_STUDIO_URL, api_key="lm-studio")


# ********************* ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ *********************

def embed(text: str) -> np.ndarray:
    """
    Получает эмбеддинг текста через LM Studio.
    Возвращает numpy-массив длины d (обычно 768).
    """
    resp = client.embeddings.create(model=EMBEDDING_MODEL, input=text)
    # resp.data[0].embedding — список из 768 float'ов
    return np.array(resp.data[0].embedding, dtype=np.float32)


def cosine(a: np.ndarray, b: np.ndarray) -> float:
    """
    Косинусное сходство двух векторов.
    Формула: cos(a, b) = (a · b) / (|a| · |b|)
    Диапазон: [-1, 1]. 1 — векторы сонаправлены, 0 — перпендикулярны.
    """
    # np.dot — скалярное произведение, np.linalg.norm — длина (норма)
    return float(np.dot(a, b) / (np.linalg.norm(a) * np.linalg.norm(b)))


def colorize(value: float) -> str:
    """
    Раскрашивает число косинусного сходства ANSI-цветом для терминала.
    Зелёный — высокое сходство, жёлтый — среднее, красный — низкое.
    """
    if value > 0.85: return f"\033[92m{value:.2f}\033[0m"   # ярко-зелёный
    if value > 0.70: return f"\033[32m{value:.2f}\033[0m"   # зелёный
    if value > 0.55: return f"\033[93m{value:.2f}\033[0m"   # жёлтый
    if value > 0.40: return f"\033[33m{value:.2f}\033[0m"   # тёмно-жёлтый
    return            f"\033[91m{value:.2f}\033[0m"          # красный


# ********************* ОСНОВНАЯ ЛОГИКА *********************

def main():
    print("=" * 70)
    print("  01_embeddings.py — знакомство с эмбеддингами")
    print("=" * 70)

    # ─── Шаг 1: получаем эмбеддинги всех фраз ──────────────────────
    print(f"\n[1/3] Получаю эмбеддинги {len(PHRASES)} фраз через LM Studio...")
    embeddings = []
    for i, phrase in enumerate(PHRASES):
        vec = embed(phrase)
        embeddings.append(vec)
        # Показываем размерность только первого — у всех одинаковая
        if i == 0:
            print(f"      Размерность вектора: d = {len(vec)}")
            print(f"      Первые 5 чисел: {vec[:5].round(3).tolist()}  ...")
        print(f"      [{i}] '{phrase}' → вектор длины {len(vec)}")

    # ─── Шаг 2: матрица попарных сходств 6×6 ───────────────────────
    print("\n[2/3] Матрица косинусных сходств:\n")

    # Печатаем заголовок: индексы столбцов
    print(" " * 4, end="")
    for j in range(len(PHRASES)):
        print(f"  [{j}]", end="")
    print()

    # Печатаем строки матрицы
    for i, vec_i in enumerate(embeddings):
        print(f"[{i}] ", end="")
        for j, vec_j in enumerate(embeddings):
            sim = cosine(vec_i, vec_j)
            print(f"  {colorize(sim)}", end="")
        # Подпись справа — какая это фраза
        print(f"   {PHRASES[i][:30]}")

    # ─── Шаг 3: семантический поиск по запросу ─────────────────────
    print(f"\n[3/3] Семантический поиск:  «{QUERY}»\n")

    query_vec = embed(QUERY)
    # Считаем сходство запроса с каждой фразой
    scores = [(i, cosine(query_vec, embeddings[i])) for i in range(len(PHRASES))]
    # Сортируем от наибольшего к наименьшему
    scores.sort(key=lambda x: x[1], reverse=True)

    print("    Результаты (от самого похожего к самому далёкому):")
    for rank, (idx, sim) in enumerate(scores, start=1):
        print(f"      #{rank}  {colorize(sim)}   '{PHRASES[idx]}'")

    print("\n" + "=" * 70)
    print("  Что мы увидели:")
    print("  • Эмбеддинг — это просто вектор из 768 чисел.")
    print("  • Косинус ≈ 0.9 у синонимов, ≈ 0.4–0.5 у разных тем.")
    print("  • Поиск по смыслу = находим вектор-сосед, не точное совпадение.")
    print("=" * 70)


if __name__ == "__main__":
    main()
